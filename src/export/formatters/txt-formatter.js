/**
 * @fileoverview Plain-text chord sheet formatter.
 * @module export/formatters/txt-formatter
 */

/**
 * @param {number} ms
 * @returns {string} "m:ss"
 */
function fmtMs(ms) {
  const s = Math.floor(ms / 1000);
  return `${Math.floor(s / 60)}:${String(s % 60).padStart(2, '0')}`;
}

/**
 * @param {import('../../types.js').ExportData} data
 * @returns {string}
 */
export function toTxt(data) {
  const { chords, meta, sections } = data;
  let out = `# ${meta.title} - ${meta.artist}\n`;
  out += `# Key: ${meta.key ?? 'Unknown'}  |  Tempo: ${meta.tempo ?? '?'} BPM\n`;
  out += `# Generated by Chorduction v${meta.version ?? __VERSION__}\n\n`;

  if (sections?.length) {
    let sIdx = 0;
    for (const chord of chords ?? []) {
      // Emit section header when we enter a new section
      while (sIdx < sections.length && chord.startMs >= sections[sIdx].endTime * 1000) { sIdx++; }
      const sec = sections[sIdx];
      if (sec && chord.startMs >= sec.startTime * 1000 && chord.startMs < sec.endTime * 1000) {
        const prev = sIdx > 0 ? sections[sIdx - 1] : null;
        if (!prev || prev.type !== sec.type || prev.repetitionIndex !== sec.repetitionIndex) {
          const label = sec.type.toUpperCase();
          const rep   = sec.repetitionIndex > 1 ? ` ${sec.repetitionIndex}` : '';
          out += `\n[${label}${rep}]  (${fmtMs(sec.startTime * 1000)} – ${fmtMs(sec.endTime * 1000)})\n`;
        }
      }
      out += `[${fmtMs(chord.startMs)}] ${chord.chord}\n`;
    }
  } else {
    for (const chord of chords ?? []) {
      out += `[${fmtMs(chord.startMs)}] ${chord.chord}\n`;
    }
  }

  return out;
}
